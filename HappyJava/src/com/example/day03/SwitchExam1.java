package com.example.day03;

public class SwitchExam1 {
    public static void main(String[] args) {
        int num = 4;
        switch (num){
            case 1 :
                System.out.println("1");
                break;
            case 2 :
                System.out.println("2");
                break;
            case 3 :
                System.out.println("3");
                break;
            case 4 :
                System.out.println("4");
                break;

            default:
                System.out.println("??");
        }
    }
}

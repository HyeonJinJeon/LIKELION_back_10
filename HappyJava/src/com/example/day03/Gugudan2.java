package com.example.day03;

public class Gugudan2 {
    public static void main(String[] args){
        for(int k = 1; k <= 9; k++) {
            for (int i = 1; i <= 9; i++) {
                System.out.println(k + " * " + i + " = " + (k * i));
            }
            System.out.println();
        }
    }
}

